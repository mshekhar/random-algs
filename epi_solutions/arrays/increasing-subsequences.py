class Solution(object):
    def dfs(self, nums, res, path, idx, prev):
        if len(path) >= 2:
            res.append(path[:])
        h_set = set()
        for i in range(idx, len(nums)):
            if nums[i] in h_set:
                continue
            h_set.add(nums[i])
            if nums[i] >= prev:
                path.append(nums[i])
                self.dfs(nums, res, path, i + 1, nums[i])
                path.pop()

    def findSubsequences(self, nums):
        """
        :type nums: List[int]
        :rtype: List[List[int]]
        """
        if len(nums) < 2:
            return []
        res = []
        path = []
        self.dfs(nums, res, path, 0, float('-inf'))
        return res


